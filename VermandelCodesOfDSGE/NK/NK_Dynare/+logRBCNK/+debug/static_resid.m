function [lhs, rhs] = static_resid(y, x, params)
T = NaN(0, 1);
lhs = NaN(12, 1);
rhs = NaN(12, 1);
lhs(1) = 0;
rhs(1) = y(3)-y(4);
lhs(2) = y(5);
rhs(2) = 1/params(5)*y(7)+params(4)*y(2);
lhs(3) = (y(3)-y(4))*params(13);
rhs(3) = params(15)*y(6);
lhs(4) = params(3)*y(9);
rhs(4) = y(8)-y(8)*(1-params(3));
lhs(5) = y(1);
rhs(5) = y(11)+y(8)*params(2)+y(7)*(1-params(2));
lhs(6) = y(10);
rhs(6) = y(6)*params(2)+y(5)*(1-params(2))-y(11);
lhs(7) = y(5)+y(7);
rhs(7) = y(6)+y(8);
lhs(8) = y(4);
rhs(8) = y(4)*params(1)+y(10)*(1-params(10))*(1-params(1)*params(10))/params(10);
lhs(9) = y(1)*params(14);
rhs(9) = y(2)*params(16)+y(9)*params(17)+params(14)*params(6)*y(12);
lhs(10) = y(3);
rhs(10) = y(3)*params(7)+(1-params(7))*y(4)*params(8)+x(3);
lhs(11) = y(11);
rhs(11) = y(11)*params(11)+x(1);
lhs(12) = y(12);
rhs(12) = y(12)*params(12)+x(2);
end
